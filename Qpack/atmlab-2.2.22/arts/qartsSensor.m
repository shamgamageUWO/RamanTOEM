'As the ARTS WSV with the same name.'
];
%-----------------------------------------------------------------------------
H.SENSOR_POL = {};
I.SENSOR_POL = [ ...
'Has the same functionality as the ARTS WSV with the same name. If set, ', ...
'*sensor_responsePolarisation* is called as first step in setting up the ', ...
'sensor response matrix.', ...
'#   Can be given either as the name of a XML file, or as a Matlab',...
' variable.'
];
